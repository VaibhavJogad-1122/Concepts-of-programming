
public class Person {
	private String name, city;
	int age; 
	
	String getName() {
		return name; 
	}
	
	String getCity() {
		return city; 
	}
	int getAge() {
		return age; 
	}
	
	void setName(String n) {
		name=n;
	};
	void setCity(String c) {
		city=c;
	}
	void setAge(int a) {
		age=a;
	}
}
